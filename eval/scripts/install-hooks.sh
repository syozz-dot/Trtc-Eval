#!/usr/bin/env bash
# Install pre-commit hook into .git/hooks/pre-commit for Trtc-Eval monorepo.
#
# Run once after clone:  ./eval/scripts/install-hooks.sh
#
# The hook runs eval/scripts/scan_sensitive.py --staged before every commit
# and blocks the commit if any API-key / OAuth-token / PII pattern is present
# in the staged diff. Matches ALLOWED_LITERALS in that script get an exemption.
set -euo pipefail

# The script lives at <repo>/eval/scripts/install-hooks.sh
# So repo root is two levels up.
REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
HOOK_PATH="$REPO_ROOT/.git/hooks/pre-commit"

if [[ ! -d "$REPO_ROOT/.git" ]]; then
  echo "error: $REPO_ROOT is not a git checkout"
  exit 1
fi

cat > "$HOOK_PATH" <<'HOOK'
#!/usr/bin/env bash
# auto-generated by eval/scripts/install-hooks.sh — do not edit here
set -euo pipefail
REPO="$(git rev-parse --show-toplevel)"
exec python3 "$REPO/eval/scripts/scan_sensitive.py" --staged
HOOK

chmod +x "$HOOK_PATH"
echo "✓ installed pre-commit hook → $HOOK_PATH"
